-- Tabla CLIENTE
CREATE TABLE CLIENTE (
  ID_CLIENTE        NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  RUT               VARCHAR2(12) NOT NULL UNIQUE,
  NOMBRE            VARCHAR2(120) NOT NULL,
  EMAIL             VARCHAR2(160) NOT NULL UNIQUE,
  CONTRASENA_HASH   BLOB NOT NULL,
  NIVEL             VARCHAR2(30) NOT NULL,
  FECHA_REGISTRO    DATE DEFAULT SYSDATE NOT NULL
);

-- Tabla PRODUCTO
CREATE TABLE PRODUCTO (
  CODIGO        VARCHAR2(30) PRIMARY KEY,
  NOMBRE        VARCHAR2(120) NOT NULL,
  PRECIO_NETO   NUMBER(12,2) NOT NULL,
  ESTADO        VARCHAR2(15) DEFAULT 'ACTIVO' NOT NULL
);

-- Tabla CARRITO
CREATE TABLE CARRITO (
  ID_CARRITO          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  ID_CLIENTE          NUMBER NOT NULL,
  FECHA_CREACION      DATE DEFAULT SYSDATE NOT NULL,
  SUBTOTAL            NUMBER(12,2) DEFAULT 0 NOT NULL,
  DESCUENTO_APLICADO  NUMBER(12,2) DEFAULT 0 NOT NULL,
  TOTAL               NUMBER(12,2) DEFAULT 0 NOT NULL,
  CONSTRAINT FK_CARRITO_CLIENTE FOREIGN KEY (ID_CLIENTE)
    REFERENCES CLIENTE(ID_CLIENTE)
);

-- Tabla ITEM_CARRITO
CREATE TABLE ITEM_CARRITO (
  ID_ITEM          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  ID_CARRITO       NUMBER NOT NULL,
  CODIGO_PRODUCTO  VARCHAR2(30) NOT NULL,
  CANTIDAD         NUMBER NOT NULL,
  SUBTOTAL_ITEM    NUMBER(12,2) DEFAULT 0 NOT NULL,
  CONSTRAINT FK_ITEM_CARRITO FOREIGN KEY (ID_CARRITO)
    REFERENCES CARRITO(ID_CARRITO),
  CONSTRAINT FK_ITEM_PRODUCTO FOREIGN KEY (CODIGO_PRODUCTO)
    REFERENCES PRODUCTO(CODIGO)
);

